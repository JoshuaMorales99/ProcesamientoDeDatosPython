# üî∂ Variables y tipos de datos.
# =====================================================================================================
"""
    Cuando hablamos de cualquier lenguaje de programacion, estos pueden aceptar diferentes tipos de datos.
    Estas variables, por ejemplo Peso, Altura, Ciudad, etc. tienen siempre un tipo de dato asociado. Python,
    como cualquier lenguaje soporta los tipos de datos mas tradicionales. Algunos de ellos son:
    - Int (Numeros entero. Ejemplo: 1, 2, 3, 4, etc.)
    - Float (Punto flotante. Ejemplo: 0.5, 1.2, 10.0, etc.)
    - Object (String. Ejemplo: "Hola", "Mundo", etc)
    - Boolean (Verdadero o Falso)
    - Datetime (Fecha)
    - Entre otros.
    Pero, ¬øQue son las variables? Las variables son espacios reservados en la memoria que, como su
    nombre indica, pueden cambiar de contenido a lo largo de la ejecucion de un programa. Estos pueden
    ser:
    - Numeros.
    - Cadenas de texto.
    - Elementos complejos.
    - Funciones.
    - Entre otros.
    En contraparte, las constantes son espacios reservados de memoria que, como su nombre indica, no
    pueden cambiar de contenido a los largo de la ejecucion de un programa.

    Algo que se debe tener en cuenta es que Python es un lenguaje case sensitive, es decir, que dicho
    lenguaje es sensible a mayusculas y munisculas. Por ejemplo:
        La variable HOLA es diferente de la variable hola.
"""
