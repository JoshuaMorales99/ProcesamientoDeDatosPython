# üî∂ ¬øQue es un programa?
# =====================================================================================================
"""
    Un programa es una secuencia de instrucciones, acciones o tareas que la computadora debe ejecutar para
    dar solucion a un problema determinado.
"""
# =====================================================================================================
# üî∂ ¬øQue es un lenguaje de programacion?
# =====================================================================================================
"""
    Un lenguaje de programacion es un conjunto de reglas, normas, simbolos y palabras especiales, ademas
    de sintaxis bien definida, utilizados para construir un programa.
"""
# =====================================================================================================
# üî∂ Clasificacion de lenguajes de programacion.
# =====================================================================================================
"""
    - Lenguaje de maquina.
        - Secuencia de numeros (1 y 0)
        - Lenguaje propio de cada computadora.
    - Lenguaje ensamblador.
        - Utiliza instrucciones y codigos especiales.
        - Varia de acuerdo al procesador.
    - Lenguaje de alto nivel.
        - Instrucciones en lenguaje familiar.
        - Utiliza otacion matematica conocida.
        - Independiente de la maquina.
        - Se dividen en declarativos y no declarativos.
"""
# =====================================================================================================
# üî∂ ¬øQue es un algoritmo?
# =====================================================================================================
"""
    Un algoritmo es un conjunto de instrucciones sistematicas y previamente definidas que se utilizan
    para realizar una determinada tarea. Estas instrucciones estan ordenadas y acotadas a manera de
    pasos a seguir para alcanzar un objetivo.
"""
# =====================================================================================================
# üî∂ Componentes basicos de un algoritmo.
# =====================================================================================================
"""
    - Entrada (Generalmente datos)
    - Proceso (Lo que el algoritmo debe hacer)
    - Salida (Resultado de la ejecucion del algoritmo)
"""
# =====================================================================================================
# üî∂ Caracteristicas generales de un algoritmo.
# =====================================================================================================
"""
    - Entrada.
    - Salida.
    - Efectividad (Resolver algo en particular)
    - Finito (No puede ser eterno, es decir, debe tener un fin)
    - Bien definido
"""
# =====================================================================================================
# üî∂ Clasificacion de un Algoritmo.
# =====================================================================================================
"""
    - Algoritmos determinista (Algoritmo que sigue una secuencia logica de pasos)
    - Algoritmos no determinista (Algoritmo que tiene bifurcaciones)
"""
