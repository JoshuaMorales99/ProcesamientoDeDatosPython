# üî∂ Data scientist o Cientifico de datos.
# =====================================================================================================
"""
    El cientifico de datos es un perfil profecional que se encarga de traducir grandes volumenes de
    informacion (Big data) en la aplicacion de un algoritmo que permita resolver una problematica o
    necesidad de negocio. Generalmente estos datos pueden tener diferente tipo de formato, es decir,
    podemos trabajar con datos estructurados, semi-estructurados y datos complejos.
    Estos profesionales pueden trabajar en cualquier tipo de negocio o industria. Justamente su objetivo
    es poder resolver una problematica a partir del desarrollo, creacion, implementacion de algoritmos
    basados en datos.
    Los dos lenguajes esenciales que se utilizan para data scientist son:
    - Python.
    - R.

    ¬øCual es su diferencia con el data analytics y el data engineer?
    Los tres profesionales trabajan con datos y sus objetivos es resolver una problematica de negocio,
    pero las soluciones que ofrecen cada uno de estos roles son distintos.
    
    El principal foco del data analytics es el negocio y la creacion de dashboard o reportes, los cuales
    son cuadros de mandos que permiten mejorar a traves de indicadores los KPI de negocio.

    El principal foco del data scientist es desarrollar modelos, crear algoritmos basados en machine
    learning. Este esta mas orientado a la estadistica, a cuestiones matematicas y ppalmente a la
    programacion.

    El principal foco del data engineer es crear la infraestructura de los datos, que, muchas veces,
    es utilizado por el cientifico de datos o por el analista de datos.

    ¬øQue hace un cientifico de datos en su dia a dia?
    Depende mucho de la empresa en particular, sin embargo, la metodologia de trabajo de un data
    scientist suele estar estructurada en, por lo menos, cuatro pasos tipicos:
    - Extraccion de datos.
    - Limpieza de los datos (data wrangling)
    - Entender los datos aplicando metodos estadisticos.
    - Creacion y optimizacion del modelo de datos del algoritmo.
"""
