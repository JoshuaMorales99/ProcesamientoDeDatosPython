# Creamos nuestra primera variable en Python y mostramos su contenido. Para mostrar cosas por pantalla,
# utilizamos la funcion print.

x = 12 # Variable numerica de tipo entero.
print(x) # Mostramos por pantalla el contenido de la variable x.

y = 0.5 # Variable numerica de tipo flotante.
print(y) # Mostramos por pantalla el contenido de la variable y.

z = True # Variable booleana
print(z) # Mostramos por pantalla el contenido de la variable z.

# Veamos el tipo de datos de nuestras variables. Para obtener el tipo de una variable, utilizamos la
# funcion type.

print(type(x)) # Mostramos por pantalla el tipo de la variable x.
print(type(y)) # Mostramos por pantalla el tipo de la variable y.
print(type(z)) # Mostramos por pantalla el tipo de la variable z.