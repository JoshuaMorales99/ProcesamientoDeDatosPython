x = 10 # Variable numerica de tipo entero.
y = 10 # Variable numerica de tipo entero.

Suma = x + y # Variable numerica de tipo entero que almacena el resultado de la operacion x + y.
print(Suma) # Mostramos por pantalla el contenido de la variable suma.

# Una mejor forma de hacer estas operaciones, sin declarar variables, seria de la siguiente manera.
print(x-y) # Mostramos por pantalla el resultado de operar la resta entre x e y.
print(x*y) # Mostramos por pantalla el resultado de operar la multiplicacion entre x e y.
print(x/y) # Mostramos por pantalla el resultado de operar la division entre x e y.
print(x**y) # Mostramos por pantalla el resultado de operar la potencia entre x e y.