# Concatenacion de cadenas de texto. Para hacer esto, utilizamos el operador +.

UnaCadena = "Hola" # Variable de tipo string.
OtraCadena = UnaCadena + " mundo!" # Varialbe de tipo string.

print(OtraCadena) # Mostamos por pantalla el contenido de la variable OtraCadena.

# Convertir texto a mayuscula o a minuscula. Para hacer esto, utilizamos la funcion upper y lower
# respectivamente.

CadenaMayuscula = OtraCadena.upper() # Variable de tipo string.
CadenaMinuscula = OtraCadena.lower() # Variable de tipo string.

print(CadenaMayuscula) # Mostamos por pantalla el contenido de la variable CadenaMayuscula.
print(CadenaMinuscula) # Mostamos por pantalla el contenido de la variable CadenaMinuscula.